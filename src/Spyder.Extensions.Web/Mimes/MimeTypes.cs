namespace Spyder.Extensions.Web.Mimes
{
    /// <summary>
    /// Known mime types of content
    /// </summary>
    public enum MimeTypes
    {
        /// <summary>
        /// Data is serialized to JSON
        /// </summary>
        Json,

        /// <summary>
        /// Data is serialized to XML
        /// </summary>
        Xml,

        /// <summary>
        /// Data is serialized as plain text
        /// </summary>
        Text
    }
}